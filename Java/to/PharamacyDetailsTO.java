package com.ehealth.to;

public class PharamacyDetailsTO {

	private String pharamacyID;
	private String pharamacyName;
	private String pharamacyRegNumber; 
	private String address;
	private String phoneNumber;
	private String password;
	private String email;
	public String getPharamacyID() {
		return pharamacyID;
	}
	public void setPharamacyID(String pharamacyID) {
		this.pharamacyID = pharamacyID;
	}
	public String getPharamacyName() {
		return pharamacyName;
	}
	public void setPharamacyName(String pharamacyName) {
		this.pharamacyName = pharamacyName;
	}
	public String getPharamacyRegNumber() {
		return pharamacyRegNumber;
	}
	public void setPharamacyRegNumber(String pharamacyRegNumber) {
		this.pharamacyRegNumber = pharamacyRegNumber;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

}
